import pandas as pd
import random
from datetime import datetime, timedelta

# Generate random dataset
apps = ['Facebook', 'Instagram', 'Twitter', 'LinkedIn', 'TikTok']
data = []

# Set parameters for random dataset generation
start_date = datetime(2023, 11, 10)
end_date = datetime(2024, 11, 21)
num_records = 1000

# Generate random records
for _ in range(num_records):
    app_name = random.choice(apps)
    usage_date = start_date + timedelta(days=random.randint(0, (end_date - start_date).days))
    # Random usage time between 1 and 120 minutes
    usage_time = random.randint(1, 120)
    suggest_question = random.choice([True, False])
    # Random boolean value
    data.append([app_name, usage_date.strftime('%Y-%m-%d'), usage_time, suggest_question])

# Create DataFrame
columns = ['App Name', 'Usage Date', 'Usage Time (mins)', 'Suggest Question (Boolean)']
df = pd.DataFrame(data, columns=columns)


# Save to CSV
df.to_csv("Adaptive_UI_Personalization_Dataset.csv", index=False)

print("Dataset generated and saved as 'Adaptive_UI_Personalization_Dataset.csv'")

